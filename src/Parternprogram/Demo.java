package Parternprogram;


public class Demo {

	public static void main(String[] args) {
	
		//sorting an int array
		int i,j,k=0;
       
		for(i=1;i<=7;i++)
		{
			int l=(i<=4)?k++:k--;
		for(j=1;j<=4;j++)
		{
			if(j<=l)
		System.out.print("*");
		
		}
		System.out.println(" ");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
